package main;

public class EscapeSequences {

	public static void main(String[] args) {
		System.out.println("The file is located at C:\\Windows\\System32\\Shell32.dll"); // Escape-sequence \\
		
		System.out.println("Patrick Henry said, \"Give me liberty, or give me death!\" in 1775."); // Escape-sequence \"
		
		System.out.println("The file is located at\n C:\\Windows\\System32\\Shell32.dll"); // Escape-sequence \n
		
		System.out.println("\nThe file is located at C:\\Windows\\System32\\Shell32.dll\n"); // Escape-sequence \n
		
		System.out.println("Name\tAge\tDate");
	}

}