package fundamentalStructures.theory;

public class primitiveOperators {
    public static void main(String[] args) {
        System.out.println("Addition, subtraction, multiplication, division, modulo.");
        System.out.println("Behavior for integer numbers.");
        System.out.println("Java has several arithmetical operations:");
        System.out.println("Addition: 7+5=" + 7 + 5);
        System.out.println("Subtraction: 7-5=" + (7 - 5));
        System.out.println("Multiplication: 7*5=" + 7 * 5);
        System.out.println("Division: 7/5=" + 7 / 5);
        System.out.println("Modulo: 7%5=" + 7 % 5);
        System.out.println("Behavior for real numbers.");
        System.out.println("Addition: 7+5=" + 7.2 + 5.1);
        System.out.println("Subtraction: 7-5=" + (7.2 - 5.1));
        System.out.println("Multiplication: 7*5=" + 7.2 * 5.1);
        System.out.println("Division: 7/5=" + 7.2 / 5.1);
        System.out.println("Modulo: 7%5=" + 7.2 % 5.1);
    }
}
