package fundamentalStructures.theory;

/**
 * Where're following modifiers:
 * \b   Backspace
 * \t   Tab
 * \n   Linefeed
 * \r   Carriage return
 * \"   Double quote
 * \'   Single quote
 * \\   Backslash
 */

public class modifiers {
    public static void main(String[] args) {
        System.out.println("b-modifier\b test");
        System.out.println("t-modifier\t test");
        System.out.println("n-modifier\n test");
        System.out.println("r-modifier\r test");
        System.out.println("quotes-modifier\" test");
        System.out.println("apostrophe-modifier\' test");
        System.out.println("slash-modifier\\ test");
    }
}
